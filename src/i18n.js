// Detect OS locale
const getDefaultLocale = () => {
  const browserLocale = navigator.language.toLowerCase()
  if (browserLocale.startsWith('de')) {
    return 'de'
  }
  if (browserLocale.startsWith('fr')) {
    return 'fr'
  }
  return 'en'
}

const i18n = createI18n({
  legacy: false,
  locale: getDefaultLocale(),
  fallbackLocale: 'en',
  messages
})

export default i18n